<?php

class Date {
    private $date;
    private $time;

    public function setDate($date) {
        $this->date = $date;
    }

    public function setTime($time) {
        $this->time = $time;
    }

    public function getDate($date) {
        $this->date = $date;
    }

    public function getTime($time) {
        $this->time = $time;
    }
}